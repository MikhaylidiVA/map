def check_user_role(user: User, required_role: str = "viewer") -> bool:
    """Check if user has the required role."""
    if user.is_superuser:
        return True
    
    # Role hierarchy: admin > editor > viewer
    role_hierarchy = {"viewer": 1, "editor": 2, "admin": 3}
    return role_hierarchy.get(required_role, 0) <= role_hierarchy.get("viewer", 0)
